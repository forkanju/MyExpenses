// Extension function to get color by letter
fun Char.getAlphabetColor(): Color {
    val index = this.uppercaseChar() - 'A'
    return if (index in 0..25) {
        alphabetColorsArray[index]
    } else {
        Color.Gray // Default color for non-alphabetic characters
    }
}

// Extension function to get color by string (first letter)
fun String.getAlphabetColor(): Color {
    return if (this.isNotEmpty()) {
        this.first().getAlphabetColor()
    } else {
        Color.Gray
    }
}

// Extension function to get light color by letter
fun Char.getAlphabetLightColor(): Color {
    val index = this.uppercaseChar() - 'A'
    return if (index in 0..25) {
        alphabetLightColorsArray[index]
    } else {
        Color.LightGray // Default color for non-alphabetic characters
    }
}

// Extension function to get light color by string (first letter)
fun String.getAlphabetLightColor(): Color {
    return if (this.isNotEmpty()) {
        this.first().getAlphabetLightColor()
    } else {
        Color.LightGray
    }
}

